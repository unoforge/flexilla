import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const ROOT_DIR = path.resolve(__dirname, "..");
const DEMO_DIR = path.join(ROOT_DIR, "src/components/docs/mdx-examples");
const OUTPUT_FILE = path.join(ROOT_DIR, "src/data/generated-code.ts");

const LANGUAGE_BY_EXTENSION = {
  ".astro": "html",
  ".html": "html",
  ".css": "css",
  ".js": "js",
  ".ts": "ts",
  ".vue": "vue",
};

async function collectFiles(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) return collectFiles(fullPath);
      return fullPath;
    }),
  );

  return files.flat();
}

function getLanguage(filePath) {
  const extension = path.extname(filePath);
  return LANGUAGE_BY_EXTENSION[extension] ?? "html";
}

function getOutputTitle(filePath) {
  const extension = path.extname(filePath);
  return extension === ".astro" ? "index.html" : path.basename(filePath);
}

async function generate() {
  const files = (await collectFiles(DEMO_DIR)).sort((a, b) => a.localeCompare(b));

  const codeEntries = await Promise.all(
    files.map(async (filePath) => {
      const source = path.basename(filePath, path.extname(filePath));
      const rawCode = await fs.readFile(filePath, "utf-8");

      return {
        source,
        title: getOutputTitle(filePath),
        icon: getLanguage(filePath),
        lang: getLanguage(filePath),
        code: rawCode,
      };
    }),
  );

  const output = `import type { SourceData } from "@/types/index";

// This file is auto-generated by docs/scripts/generate-manifest.js.
// Do not edit it manually.
export const codeData: SourceData = ${JSON.stringify(
    Object.fromEntries(
      codeEntries.map(({ source, ...snippet }) => [source, snippet]),
    ),
    null,
    2,
  )};
`;

  await fs.mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
  await fs.writeFile(OUTPUT_FILE, output, "utf-8");

  console.log(`Generated ${codeEntries.length} code snippets in ${OUTPUT_FILE}`);
}

generate().catch((error) => {
  console.error(error);
  process.exit(1);
});
